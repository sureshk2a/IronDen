# ── All programs combined ─────────────────────────────────────────────────────
ALL_PROGRAMS = LEAN_AND_MEAN + PURE_STRENGTH + FAT_BURNER + BODY_RECOMP + BEGINNER_BASICS + UPPER_LOWER


def run_seed(db: Session) -> None:
    # Idempotency check: use (program, day_number) tuples
    existing = {
        (r[0], r[1])
        for r in db.query(WorkoutTemplate.program, WorkoutTemplate.day_number).all()
    }
    new_plans = [p for p in ALL_PROGRAMS if (p["program"], p["day_number"]) not in existing]

    if not new_plans:
        logger.info("Database already seeded — skipping.")
        return

    logger.info("Seeding %d new workout day(s)…", len(new_plans))
    for plan in new_plans:
        exercises_data = plan["exercises"]
        template_data = {k: v for k, v in plan.items() if k != "exercises"}
        template = WorkoutTemplate(**template_data)
        db.add(template)
        db.flush()

        for ex_data in exercises_data:
            exercise = Exercise(workout_template_id=template.id, **ex_data)
            db.add(exercise)

    db.commit()
    logger.info("Seed complete — %d programs, %d total workout days.", len(set(p["program"] for p in ALL_PROGRAMS)), len(ALL_PROGRAMS))
